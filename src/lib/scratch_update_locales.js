locales.forEach((loc) => {
  const filePath = path.join(__dirname, "..", "locales", loc, "common.json");
  if (!fs.existsSync(filePath)) {
    console.error(`File not found: ${filePath}`);
    return;
  }

  const raw = fs.readFileSync(filePath, "utf-8");
  const data = JSON.parse(raw);

  // 1. Update intelligence keys
  if (!data.intelligence) {
    data.intelligence = {};
  }
  Object.keys(extraIntelligenceKeys).forEach((key) => {
    data.intelligence[key] = extraIntelligenceKeys[key][loc] || extraIntelligenceKeys[key]["en"];
  });

  // 2. Append new sections
  Object.keys(newSections).forEach((section) => {
    if (!data[section]) {
      data[section] = {};
    }
    Object.keys(newSections[section]).forEach((key) => {
      data[section][key] = newSections[section][key][loc] || newSections[section][key]["en"];
    });
  });

  // Write back nicely formatted
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2), "utf-8");
  console.log(`Updated ${loc}/common.json`);
});
